
public class Console {

	public void println(String line) {
		System.out.println(line);
	}
	
	public void println() {
		System.out.println();
	}
	
	public void print(String line) {
		System.out.print(line);
	}
	
	public void print(char ch) {
		System.out.print(ch);
	}
	
	public void println(char ch) {
		System.out.println(ch);
	}
}
